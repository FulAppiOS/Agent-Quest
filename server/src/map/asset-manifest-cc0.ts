/**
 * Manifest for the `tiny-swords-cc0` editor theme. Shape mirrors the
 * default theme so every consumer (TilePalette, EditorScene, etc.)
 * sees the same record layout — only the paths differ.
 *
 * Tilesets + protected buildings + NPC sprites are still hand-curated
 * because they carry pack-specific semantics (activity mappings, frame
 * layouts) that can't be inferred from filenames alone. Decorations, on
 * the other hand, are generated by walking the upstream folder tree and
 * applying declarative metadata rules (see `cc0-pack-metadata.ts`).
 */

const CC0_BASE = '/assets/themes/tiny-swords-cc0';
const TROOPS = `${CC0_BASE}/Factions/Knights/Troops`;
const GOBLINS = `${CC0_BASE}/Factions/Goblins/Troops`;
const BUILDINGS = `${CC0_BASE}/Factions/Knights/Buildings`;
const GOBLIN_HOUSE = `${CC0_BASE}/Factions/Goblins/Buildings/Wood_House`;
/** User-authored custom building PNGs. Replace the stock CC0 buildings
 * for all 8 agent activities. */
const CUSTOM_BUILDINGS = `${CC0_BASE}/BuildingsCustom`;

/** Units in the Goblins faction — sheet layout (192px frames) differs per
 * unit, but all share the same parent folder structure. */
const GOBLIN_UNITS: readonly UnitType[] = ['tnt', 'torch'] as const;
const isGoblin = (u: UnitType): boolean => (GOBLIN_UNITS as readonly UnitType[]).includes(u);

/** Directory (and filename stem) for each goblin unit — note `TNT` is
 * all-caps in the upstream pack, so `cap()` can't derive it. */
const GOBLIN_DIR: Record<'tnt' | 'torch', string> = {
  tnt: 'TNT',
  torch: 'Torch',
};

/** Absolute filesystem path to the CC0 theme root. Resolved relative to
 * this file so `bun test` and `bun start` both find the assets without
 * depending on cwd. */
const CC0_ABS_ROOT = join(import.meta.dir, '../../../client/public/assets/themes/tiny-swords-cc0');
const CC0_WEB_ROOT = '/assets/themes/tiny-swords-cc0';

