/** Per-unit sheet shape for the CC0 heroes — mirrors SHEET_SPEC on the
 * client. Rows are animations, columns are frames. */
const CC0_SPEC: Record<UnitType, {
  sheetCols: number;
  idleFrames: number;
  runFrames: number;
  idleFrameIndices: number[];
  runFrameIndices: number[];
}> = {
  warrior: { sheetCols: 6, idleFrames: 6, runFrames: 6, idleFrameIndices: [0, 1, 2, 3, 4, 5], runFrameIndices: [6, 7, 8, 9, 10, 11] },
  archer:  { sheetCols: 8, idleFrames: 6, runFrames: 6, idleFrameIndices: [0, 1, 2, 3, 4, 5], runFrameIndices: [8, 9, 10, 11, 12, 13] },
  pawn:    { sheetCols: 6, idleFrames: 6, runFrames: 6, idleFrameIndices: [0, 1, 2, 3, 4, 5], runFrameIndices: [6, 7, 8, 9, 10, 11] },
  // Goblins — each sheet is 192px frames, rows = animation, cols = frame.
  // tnt/torch sheets are 7 cols × N rows, but per Tiny Swords convention
  // only the first 6 cols of each row are populated (col 6 is padding).
  // Using 7 frames would leave a blank frame per cycle → visible flicker.
  tnt:     { sheetCols: 7, idleFrames: 6, runFrames: 6, idleFrameIndices: [0, 1, 2, 3, 4, 5], runFrameIndices: [7, 8, 9, 10, 11, 12] },
  torch:   { sheetCols: 7, idleFrames: 6, runFrames: 6, idleFrameIndices: [0, 1, 2, 3, 4, 5], runFrameIndices: [7, 8, 9, 10, 11, 12] },
};

const ALL_UNIT_COLORS: UnitColor[] = ['blue', 'red', 'black', 'yellow', 'purple'];
const ALL_UNIT_TYPES: UnitType[] = ['warrior', 'archer', 'pawn', 'tnt', 'torch'];

function resolveUnit(unit: UnitType): UnitType {
  // All CC0 units have dedicated assets now — pass through unchanged.
  return unit;
}
function resolveColor(color: UnitColor): UnitColor {
  return color === 'black' ? 'purple' : color;
}

function cap(s: string): string {
  return s[0]!.toUpperCase() + s.slice(1);
}

function spritePath(color: UnitColor, unit: UnitType): string {
  const ru = resolveUnit(unit);
  const rc = resolveColor(color);
  if (isGoblin(ru)) {
    // Goblins live under Factions/Goblins/Troops and have no filename
    // quirks; TNT is all-caps in the pack so we can't use `cap()` here.
    const unitDir = GOBLIN_DIR[ru as 'tnt' | 'torch'];
    const colorDir = cap(rc);
    return `${GOBLINS}/${unitDir}/${colorDir}/${unitDir}_${colorDir}.png`;
  }
  const fileColor = ru === 'archer' && rc === 'purple' ? 'Purlple' : cap(rc);
  return `${TROOPS}/${cap(ru)}/${cap(rc)}/${cap(ru)}_${fileColor}.png`;
}

function buildNpcSprites(): NpcSpriteManifest[] {
  const out: NpcSpriteManifest[] = [];
  for (const color of ALL_UNIT_COLORS) {
    for (const unit of ALL_UNIT_TYPES) {
      const spec = CC0_SPEC[resolveUnit(unit)];
      // Logical (color, unit) key — the UI consumes these even when they
      // fall back to a shared resolved texture. Point idle/run paths to
      // the same combined sheet (the CC0 pack has one file per pair).
      const path = spritePath(color, unit);
      const idleKey = `cc0-${resolveColor(color)}-${resolveUnit(unit)}-idle`;
      const runKey  = `cc0-${resolveColor(color)}-${resolveUnit(unit)}-run`;
      out.push({
        unit,
        color,
        idleKey,
        runKey,
        idlePath: path,
        runPath: path,
        idleFrames: spec.idleFrames,
        runFrames: spec.runFrames,
        frameWidth: 192,
        frameHeight: 192,
        idleFrameIndices: spec.idleFrameIndices,
        runFrameIndices: spec.runFrameIndices,
      });
    }
  }
  return out;
}

export function buildAssetManifestCc0(): AssetManifest {
  return {
    tilesets: buildTilesets(),
    decorations: [...buildCc0DecorationsFromScan(), ...buildLegacyAliases()],
    protectedBuildings: buildProtectedBuildings(),
    npcSprites: buildNpcSprites(),
  };
}
